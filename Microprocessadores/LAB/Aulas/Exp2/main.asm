PORT_WR	EQU	4000H
PORT_RD	EQU	4000H

.chip 8051

INICIO: MOV	DPTR, #PORT_RD
				MOVX	A, @DPTR
				ANL		A, #0FH
				CALL	CONV
				MOV		DPTR, #PORT_WR
				MOVX	@DPTR, A
				SJMP	INICIO
				
TABLE:	DB		0011111b
				DB		0000110b	
				DB		1011011b
				DB		1001111b
				DB		1100110b
				DB		1101101b
				DB		1111100b
				DB		0000111b
				DB		1111111b
				DB		1100111b
				
CONV:		MOV		DPTR, #TABLE
				MOVC	A, @A+DPTR
				CPL		A
				RET
				
